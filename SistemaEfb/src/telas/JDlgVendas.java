    public void limpar() {
        jTxtId.setText("");
        jCboCliente.setSelectedIndex(-1);
        jCboVendedor.setSelectedIndex(-1);
        jFmtData.setText("");
        jFmtTotal.setText("0.0");
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            vendasProdControle.removeList(i);
        }

    }

    public VendaEfb viewBean() {
        venda.setIdvendaEfb(Integer.valueOf(jTxtId.getText()));

        ClienteEfb cliente = (ClienteEfb) jCboCliente.getSelectedItem();
        venda.setClienteEfb(cliente);
        VendedorEfb vendedor = (VendedorEfb) jCboVendedor.getSelectedItem();
        venda.setVendedorEfb(vendedor);

        try {
            venda.setDataVendaEfb(formato.parse(jFmtData.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(JDlgVendas.class.getName()).log(Level.SEVERE, null, ex);
        }
        venda.setTotalEfb(Double.valueOf(jFmtTotal.getText()));

        return venda;
    }

    public void beanView(VendaEfb venda) {
        jTxtId.setText(String.valueOf(venda.getIdvendaEfb()));
        jFmtData.setText(formato.format(venda.getDataVendaEfb()));
        jFmtTotal.setText(String.valueOf(venda.getTotalEfb()));
        ClienteEfb cliente = new ClienteEfb();

        jCboCliente.setSelectedItem(venda.getClienteEfb());
        jCboVendedor.setSelectedItem(venda.getVendedorEfb());
    }


    private void jBtnConfirmar_efbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmar_efbActionPerformed
        // TODO add your handling code here:
        if (getTitle().equals("Incluindo")) {
            vendedorEfb = viewBean();
            vendedorDao_efb.insert(vendedorEfb);
            util.mensagem("Incluindo");
            setVisible(false);
        } else if (getTitle().equals("Alterando")) {
            vendedorDao_efb.update(vendedorEfb);
            util.mensagem("Alterando");
            jTxtCodigoVendedor_efb.setEnabled(false);
            setVisible(false);
        } else {
            util.mensagem("ERRO");
        }
    }//GEN-LAST:event_jBtnConfirmar_efbActionPerformed


    public void beanView(VendedorEfb vendedor) {
        jTxtCodigoVendedor_efb.setText(String.valueOf(vendedor.getIdvendedorEfb()));
        jTxtNome_efb.setText(vendedor.getNomeEfb());
        jTxtSobrenome_efb.setText(vendedor.getSobrenomeEfb());
        jFmtCpf_efb.setText(vendedor.getCpfEfb());
        jFmtRg_efb.setText(vendedor.getRgEfb());
        jTxtEndereco_efb.setText(vendedor.getEnderecoEfb());
        jChbAtivo_efb.setSelected("Sim".equals(vendedor.getAtivoEfb()));
    }

    public VendedorEfb viewBean() {
        vendedorEfb.setIdvendedorEfb(Integer.parseInt(jTxtCodigoVendedor_efb.getText()));
        vendedorEfb.setNomeEfb(jTxtNome_efb.getText());
        vendedorEfb.setSobrenomeEfb(jTxtSobrenome_efb.getText());
        vendedorEfb.setCpfEfb(jFmtCpf_efb.getText());
        vendedorEfb.setRgEfb(jFmtRg_efb.getText());
        vendedorEfb.setEnderecoEfb(jTxtEndereco_efb.getText());
        vendedorEfb.setAtivoEfb(jChbAtivo_efb.isSelected() ? "Sim" : "Não");
        return vendedorEfb;
    }

